import copy

def intcode_computer(intcode, debug = False):
	opCodePos = 0
	opCode = int(intcode[opCodePos])
	step = 0
	while opCode != 99:
		step += 1

		num1Pos = int(intcode[opCodePos + 1])
		num2Pos = int(intcode[opCodePos + 2])
		resPos = int(intcode[opCodePos + 3])

		num1 = int(intcode[num1Pos])
		num2 = int(intcode[num2Pos])

		result = 0
		if opCode == 1:
			result = num1 + num2
		elif opCode == 2:
			result = num1 * num2

		if debug:
			print('Step ' + str(step))
			print('Op Code: ' + str(opCode))
			print('num1 Pos: ' + str(num1Pos))
			print('num2 Pos: ' + str(num2Pos))
			print('result Pos: ' + str(resPos))
			print('num1: ' + str(num1))
			print('num2: ' + str(num2))
			print('result: ' + str(result))
			print('=============================')

		intcode[resPos] = result

		opCodePos += 4
		opCode = int(intcode[opCodePos])

	if debug:
		print("Result Array:")
		print(intcode)

	return intcode[0]


